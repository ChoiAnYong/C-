#include<iostream>
using namespace std;
#define FI 3.14
class Circle {
	int radius;
public:
	void setRadius(int radius) { this->radius = radius; }
	double getArea() {
		return radius * radius*FI;
	}
};

int main() {
	int n;
	cout << "원의 개수 >> ";
	cin >> n;
	Circle *arr = new Circle[n];
	int r, count = 0;
	for (int i = 0; i < n; i++) {
		cout << "원 " << i + 1 << "의 반지름 >> ";
		cin >> r;
		arr[i].setRadius(r);
		if (arr[i].getArea() > 100)count++;
	}
	cout << "면적이 100보다 큰 원은 " << count << "개 입니다." << endl;
}